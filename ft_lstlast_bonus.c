#include <stdio.h>
#include <stdlib.h>
#include "libft.h"

/**
 * Ft_lstlast returns the last element of a linked list.
 * 
 * @param lst A pointer to the first link of a list.
 * 
 * @return The last element of the list.
 */
t_list	*ft_lstlast(t_list *lst)
{
	if (!lst)
		return (NULL);
	while (lst->next)
		lst = lst->next;
	return (lst);
}
